        /// <summary>
        /// 获得数据列表
        /// </summary>
        public List<Model.Pchannel> DataTableToList(DataTable dt)
        {
            List<Model.Pchannel> modelList = new List<Model.Pchannel>();
            int rowsCount = dt.Rows.Count;
            if (rowsCount > 0)
            {
                Model.Pchannel model;
                for (int n = 0; n < rowsCount; n++)
                {
                    model = dal.DataRowToModel(dt.Rows[n]);
                    if (model != null)
                    {
                        modelList.Add(model);
                    }
                }
            }
            return modelList;
        }

